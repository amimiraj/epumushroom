                <h2><b>Other benefits</b></h2><br>
                Mushrooms are rich in B vitamins, such as:<br>
                ●	riboflavin, or B-2<br>
                ●	folate, or B-9<br>
                ●	thiamine, or B-1<br>
                ●	pantothenic acid, or B-5<br>
                ●	niacin, or B-3<br>
                B vitamins help the body get energy from food and form red blood cells. A number of B vitamins also appear to be important for a healthy brain.<br>
                The choline in mushrooms can help with muscle movement, learning, and memory. Choline assists in maintaining the structure of cellular membranes and plays a role in the transmission of nerve impulses.<br>
                Mushrooms are also the only vegan, non fortified dietary source of vitamin D.<br>
                Several other minerals that may be difficult to obtain from a vegan diet — such as selenium, potassium, copper, iron, and phosphorus — are available in mushrooms.<br><br>


